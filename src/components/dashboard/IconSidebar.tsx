export const IconSidebar = () => {
    const location = useLocation();
    const isProfilePage = location.pathname === '/profile';

    // Theme state - check localStorage or prefer system setting
    const [isDarkMode, setIsDarkMode] = useState(() => {
        const saved = localStorage.getItem('theme');
        if (saved) return saved === 'dark';
        return window.matchMedia('(prefers-color-scheme: dark)').matches;
    });

    // Apply theme to document
    useEffect(() => {
        document.documentElement.setAttribute('data-theme', isDarkMode ? 'dark' : 'light');
        localStorage.setItem('theme', isDarkMode ? 'dark' : 'light');
    }, [isDarkMode]);

    const toggleTheme = () => setIsDarkMode(prev => !prev);

    return (
        <aside className="icon-sidebar">
            <div className="icon-sidebar__items">
                {sidebarItems.map((item) => {
                    const isActive = item.id === 'home'
                        ? location.pathname === '/'
                        : isProfilePage && item.id === 'activity';

                    return (
                        <Link
                            key={item.id}
                            to={item.path}
                            className={`icon-sidebar__item ${isActive ? 'icon-sidebar__item--active' : ''}`}
                            title={item.label}
                        >
                            {item.icon}
                        </Link>
                    );
                })}
            </div>

            <div className="icon-sidebar__divider" />

            <button
                className="icon-sidebar__theme-toggle"
                onClick={toggleTheme}
                title={isDarkMode ? 'Switch to Light Mode' : 'Switch to Dark Mode'}
                aria-label={isDarkMode ? 'Switch to Light Mode' : 'Switch to Dark Mode'}
            >
                {isDarkMode ? (
                    // Sun icon for switching to light mode
                    <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                        <circle cx="12" cy="12" r="5" />
                        <line x1="12" y1="1" x2="12" y2="3" />
                        <line x1="12" y1="21" x2="12" y2="23" />
                        <line x1="4.22" y1="4.22" x2="5.64" y2="5.64" />
                        <line x1="18.36" y1="18.36" x2="19.78" y2="19.78" />
                        <line x1="1" y1="12" x2="3" y2="12" />
                        <line x1="21" y1="12" x2="23" y2="12" />
                        <line x1="4.22" y1="19.78" x2="5.64" y2="18.36" />
                        <line x1="18.36" y1="5.64" x2="19.78" y2="4.22" />
                    </svg>
                ) : (
                    // Moon icon for switching to dark mode
                    <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                        <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z" />
                    </svg>
                )}
            </button>
        </aside>
    );
};
